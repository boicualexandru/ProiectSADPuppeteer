function editDistance(s1, s2) {
    s1 = s1.toLowerCase();
    s2 = s2.toLowerCase();

    var costs = new Array();
    for (var i = 0; i <= s1.length; i++) {
        var lastValue = i;
        for (var j = 0; j <= s2.length; j++) {
            if (i == 0)
                costs[j] = j;
            else {
                if (j > 0) {
                    var newValue = costs[j - 1];
                    if (s1.charAt(i - 1) != s2.charAt(j - 1))
                        newValue = Math.min(Math.min(newValue, lastValue),
                            costs[j]) + 1;
                    costs[j - 1] = lastValue;
                    lastValue = newValue;
                }
            }
        }
        if (i > 0)
            costs[s2.length] = lastValue;
    }
    return costs[s2.length];
}

module.exports.similarity = (s1, s2) => {
    var longer = s1;
    var shorter = s2;
    if (s1.length < s2.length) {
        longer = s2;
        shorter = s1;
    }
    var longerLength = longer.length;
    if (longerLength == 0) {
        return 1.0;
    }
    return (longerLength - editDistance(longer, shorter)) / parseFloat(longerLength);
}

module.exports.getMostSimilarModelId = (model) => {
    var bestModelId = -1;
    var bestModelName = '';
    var bestSimilarity = 0;
    for(var index = 0; index < models.length ; index++){
        var similarity = module.exports.similarity(model, models[index].Name);
        if(similarity == 1) return models[index].Id;

        if(similarity > bestSimilarity) {
            bestSimilarity = similarity;
            bestModelName = models[index].Name;
            bestModelId = models[index].Id;
        }
    }
    
    // if(bestSimilarity < 0.8) 
    console.warn(model + ' model best guess is ' + bestModelName + '. Similarity: ' + bestSimilarity);

    return bestModelId;
}

